package com.dgmf.variables;

public class Variables {
    public static void main(String[] args) {
        int mathMarks = 70;

        System.out.println(mathMarks);
        System.out.println("I scored " + mathMarks + " in Maths");

        mathMarks = 45;

        System.out.println("I have now scored " + mathMarks + " in Maths");
    }
}
